#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	int i;

	i = 0;
	while (*src && n--)
		dst[i++] = *src++;
	n++;
	if (n)
	{
		while (n - 1)
		{
			dst[i++] = '\0';
			n--;
		}
	}
	return (dst);
}
